/**
 * Transforms selected frames (that are inside another frame) into Slices.
 * Useful for asset export optimization where children of a main container
 * needs to be flattened into exportable slices at the same positions.
 */
export async function selectionToSlices() {
  const selection = figma.currentPage.selection;
  if (selection.length === 0) {
    figma.notify('Selecione um ou mais frames para converter em slices ✂️');
    return;
  }

  // Sort selection by position (X then Y) for consistency
  const sorted = [...selection].sort((a, b) => {
    if (Math.abs(a.y - b.y) < 10) return a.x - b.x;
    return a.y - b.y;
  });

  const createdSlices: SliceNode[] = [];

  for (const node of sorted) {
    if (('width' in node) && node.parent) {
      const parent = node.parent;
      const slice = figma.createSlice();
      
      // Add to parent FIRST (Figma requires parent to be set before checking/setting layoutPositioning)
      if ('appendChild' in parent) {
        parent.appendChild(slice);
      }

      // Check if parent is an Auto Layout frame
      if (parent.type === 'FRAME' && parent.layoutMode !== 'NONE') {
        if ('layoutPositioning' in slice) {
          (slice as any).layoutPositioning = 'ABSOLUTE';
        }
      }

      // Preserve your custom names (e.g. "01 / Capa")
      slice.name = node.name;
      
      // Now position it exactly over the source node
      slice.x = node.x;
      slice.y = node.y;
      slice.resize(node.width, node.height);
      
      // High Quality Instagram Settings (@2x PNG)
      slice.exportSettings = [{
        format: 'PNG',
        suffix: '',
        constraint: { type: 'SCALE', value: 2 },
        contentsOnly: false // Captures seamless background from parent
      }];

      createdSlices.push(slice);
    }
  }

  if (createdSlices.length > 0) {
    figma.currentPage.selection = createdSlices;
    figma.notify(`✨ ${createdSlices.length} Slices sobrepostos com sucesso!`);
  } else {
    figma.notify('⚠ Selecione os frames do carrossel.');
  }
}
